        // Testimoni Slider Functionality - IMPROVED
        document.addEventListener('DOMContentLoaded', function() {
            const track = document.getElementById('testimoniTrack');
            const slides = document.querySelectorAll('.testimoni-slide');
            const dots = document.querySelectorAll('.dot');
            const prevBtn = document.getElementById('prevBtn');
            const nextBtn = document.getElementById('nextBtn');
            
            let currentSlide = 0;
            const slidesToShow = getSlidesToShow();
            
            function getSlidesToShow() {
                if (window.innerWidth < 768) return 1;
                if (window.innerWidth < 1024) return 2;
                return 3;
            }
            
            function updateSlider() {
                const slideWidth = slides[0].offsetWidth + 32; // width + gap
                track.style.transform = `translateX(-${currentSlide * slideWidth}px)`;
                
                // Update dots
                dots.forEach((dot, index) => {
                    dot.classList.toggle('active', index === currentSlide);
                });
            }
            
            // Next button
            nextBtn.addEventListener('click', () => {
                const maxSlide = slides.length - getSlidesToShow();
                currentSlide = currentSlide >= maxSlide ? 0 : currentSlide + 1;
                updateSlider();
            });
            
            // Previous button
            prevBtn.addEventListener('click', () => {
                const maxSlide = slides.length - getSlidesToShow();
                currentSlide = currentSlide <= 0 ? maxSlide : currentSlide - 1;
                updateSlider();
            });
            
            // Dot navigation
            dots.forEach((dot, index) => {
                dot.addEventListener('click', () => {
                    currentSlide = index;
                    updateSlider();
                });
            });
            
            // Auto slide (optional)
            let slideInterval = setInterval(() => {
                const maxSlide = slides.length - getSlidesToShow();
                currentSlide = currentSlide >= maxSlide ? 0 : currentSlide + 1;
                updateSlider();
            }, 5000);
            
            // Pause auto slide on hover
            track.addEventListener('mouseenter', () => {
                clearInterval(slideInterval);
            });
            
            track.addEventListener('mouseleave', () => {
                slideInterval = setInterval(() => {
                    const maxSlide = slides.length - getSlidesToShow();
                    currentSlide = currentSlide >= maxSlide ? 0 : currentSlide + 1;
                    updateSlider();
                }, 5000);
            });
            
            // Touch swipe support for mobile
            let startX = 0;
            let endX = 0;
            
            track.addEventListener('touchstart', (e) => {
                startX = e.touches[0].clientX;
            });
            
            track.addEventListener('touchmove', (e) => {
                endX = e.touches[0].clientX;
            });
            
            track.addEventListener('touchend', () => {
                handleSwipe();
            });
            
            function handleSwipe() {
                const swipeThreshold = 50;
                
                if (startX - endX > swipeThreshold) {
                    // Swipe left - next slide
                    const maxSlide = slides.length - getSlidesToShow();
                    currentSlide = currentSlide >= maxSlide ? 0 : currentSlide + 1;
                } else if (endX - startX > swipeThreshold) {
                    // Swipe right - previous slide
                    const maxSlide = slides.length - getSlidesToShow();
                    currentSlide = currentSlide <= 0 ? maxSlide : currentSlide - 1;
                }
                
                updateSlider();
            }
            
            // Handle window resize
            window.addEventListener('resize', () => {
                updateSlider();
            });
            
            // Initialize slider
            updateSlider();
        });

